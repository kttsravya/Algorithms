package Outco_DS3_HashTable;

public class KeyValueObject {
	int key;
	String value;

	public KeyValueObject(int key1, String value1) {
		this.key = key1;
		this.value = value1;
	}

	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
